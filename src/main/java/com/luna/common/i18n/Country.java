    /** 通过code获得国家 */
    public static String getName(String code) {
        for (Country c : Country.values()) {
            if (c.getcode() == code) {
                return c.name;
            }
        }
        return null;
    }

    /** 通过国家获得二级编码 */
    public static String getCodeByName(String name) {
        for (Country c : Country.values()) {
            if (c.getName().equals(name)) {
                return c.toString();
            }
        }
        return null;
    }

